package HomeWork12;

public class RelationalOperators {
    public static void main(String[] args) {

//Java operators practice 1

        int a = 90;
        int b = 65;
        System.out.println(a == b); // returns false because is not equal to 90
        System.out.println(a != b); // returns true because 10 is not equal to 65
        System.out.println(a > b); // returns true
        System.out.println(a < b); // returns false
        System.out.println(a >= b); // returns true
        System.out.println(a <= b); // returns false
        // Practice 2

        int C = 151;
        int D = 300;
        System.out.println(C == D); // returns false because is not equal to 151
        System.out.println(C != D); // returns true because 151 is not equal to 300
        System.out.println(C > D); // returns False
        System.out.println(C < D); // returns true
        System.out.println(C >= D); // returns false
        System.out.println(C <= D); // returns true
    }
}
